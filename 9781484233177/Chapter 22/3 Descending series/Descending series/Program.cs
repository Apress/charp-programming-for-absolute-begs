using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Descending_series
{
    class Program
    {
        static void Main(string[] args)
        {
            // Output
            for (int number = 10; number >= 1; number--)
            {
                Console.WriteLine(number);
            }

            // Waiting for Enter
            Console.ReadLine();
        }
    }
}
